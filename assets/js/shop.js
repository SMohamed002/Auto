// add to cart count
var mainCount = document.querySelectorAll(".the-No");
var incrementBtn = document.querySelectorAll(".add-qty");
var decrementBtn = document.querySelectorAll(".neg");

incrementBtn.forEach((btn, index) => {
  btn.onclick = function () {
    mainCount[index].textContent = parseInt(mainCount[index].textContent) + 1;
  };
});

// Loop through decrement buttons
decrementBtn.forEach((btn, index) => {
  btn.onclick = function () {
    if (parseInt(mainCount[index].textContent) > 0) {
      mainCount[index].textContent = parseInt(mainCount[index].textContent) - 1;
    }
  };
});

function addEventToAddToCart() {
  const addToCartBtns = document.querySelectorAll(".addcart");

  addToCartBtns.forEach((btn) => {
    btn.addEventListener("click", () => {
      const parent = btn.closest(".products-box");
      const id = parent.dataset.id;

      const product = products.find((product) => product.id == id);

      const cartProduct = cart.find((product) => product.id == id);

      if (cartProduct == undefined) {
        cart.push({ ...product, quantity: 1 });
      } else {
        cartProduct.quantity++;
      }

      localStorage.setItem("cart", JSON.stringify(cart));
      productNum.textContent = cart.length;
    });
  });
}
